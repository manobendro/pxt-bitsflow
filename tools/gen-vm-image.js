#!/usr/bin/env node
// Turn a compiled VM image (binary.pxt64) into a C source file that the RP2040
// firmware embeds in flash. Produces `bitsflow_vm_image[]` + `bitsflow_vm_image_len`
// (C linkage), matching the externs in firmware/rp2040/src/rp2040_main.cpp.
//
//   usage: node tools/gen-vm-image.js [input.pxt64] [output.c]
//   defaults: projects/blink/built/binary.pxt64  ->  firmware/rp2040/generated/vm_image.c
"use strict";
const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const input = process.argv[2] || path.join(root, "projects/blink/built/binary.pxt64");
const output = process.argv[3] || path.join(root, "firmware/rp2040/generated/vm_image.c");

if (!fs.existsSync(input)) {
    console.error(`ERROR: VM image not found: ${input}`);
    console.error(`Build a project first (e.g. tools/docker-vm.ps1 -Project projects/blink).`);
    process.exit(1);
}

const data = fs.readFileSync(input);
let out = "// Auto-generated by tools/gen-vm-image.js. Do not edit.\n";
out += `// Source: ${path.basename(input)} (${data.length} bytes)\n`;
out += "#include <stdint.h>\n\n";
out += "// 8-byte aligned so the VM image can be used in place if ever needed.\n";
out += "const unsigned char bitsflow_vm_image[] __attribute__((aligned(8))) = {\n";
for (let i = 0; i < data.length; i++) {
    out += "0x" + data[i].toString(16).padStart(2, "0") + ",";
    out += (i % 16 === 15) ? "\n" : " ";
}
out += "\n};\n";
out += `const unsigned int bitsflow_vm_image_len = ${data.length};\n`;

fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, out);
console.log(`wrote ${output} (${data.length} bytes embedded)`);
